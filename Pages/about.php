                        Zonder mijzelf de mogelijkheid geven om te dromen zal er nooit iets concreets uit mijn handen vloeien. Het denken in duizend onmogelijke oplossingen voor een ontwerp is zeer belangrijk, op de lagere school werd mij aangeleerd dat maar een antwoord goed is. Langzaam maar zeker leer ik deze diepgewortelde beperking af en geef ik me over aan het creatieve proces.
                        In mijn werk ben ik constant bezig met de zoektocht naar het visualiseren van een concept of object. Ik wil me niet binden aan een enkel materiaal, maar toch val ik vaak terug in het gebruiken van wat er al is. Gebruikte object hebben voor mij een bepaalde meer waarde, je voelt bijna de verloren aanwezigheid van de vorige eigenaar.
                        Elk object dat ik maak, elk concept wat ik uitdenk vind zijn oorsprong in het verleden. Dat wat geweest is, kan een voorboden zijn voor de toekomst. Ik geloof dat de geschiedenis zich herhaald op bepaalde wijze, en dat er bij elke stroming een tegengeluid nodig is. Dit houdt volgens mij het geheel in balans en kan leiden tot innovatie.
                        Persoonlijk ben ik eigenlijk tegen alles maar ik zie ook overal iets in, ik kan me goed vinden in de filosofie van de DADA stroming terwijl ik, er tegelijk tijd niks mee heb. Ik classificeer mijzelf niet als een vakman, als een doe’er, als een denker of als een designer. Ik zie mijzelf liever als een autonoom persoon die overal een afkeer van wilt hebben maar stiekem ook alles aantrekkelijk vind.
                        Ik zie het nut in van werken aan de hand van een concept en het documenteren van processen, dit kan extra waarde geven aan het object. Maar het mag nooit te kosten gaan van het daadwerkelijke resultaat, omdat ik denk dat een concreet eindresultaat het hoogst haalbare is.
                        Mijn motivatie om te creëren vloeit voort uit het verleggen van mijn kunnen. Vaak is voldoening heel ver te zoeken, echte oprechte voeldoening ervaar ik maar een aantal keer per jaar. Het ultieme gevoel van voldoening word pas bereikt als een willekeurig persoon, een object van mijn hand plaatst in zijn of haar meest persoonlijke omgeving. Mits dit totaal is gemaakt zonder restricties, het binnendringen van het betreffende interieur en op eigen wijze er een persoonlijke twist aangeven is voor mij het hoogst haalbare.
