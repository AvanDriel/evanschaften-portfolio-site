            De vorm van het object is gebaseerd op een detail van de oude stadskaart van Arnhem. De verwisselbare sculpturen zijn ge√Ønspireerd op het plafon van de Eusebiuskerk.
            De bezoeker heeft de mogelijkheid om zelf de samenstelling van het object te veranderen. De sculpturen kunnen naar eigen inzicht in de gaten geplaatst worden. Wanneer de kleur combinatie klopt, zal een leesbare vraag verschijnen. De vragen zijn gebaseerd op het verleden en de toekomst. Het uitgangspunt van deze vragen is: sociale interactie, verdieping en het bevestigen van de realiteit. Deze zijn gebaseerd op verlangens, herinneringen en dromen.

            Het gebruik van het object kan leiden tot eerlijke, grappige of emotionele gesprekken, en wellicht tot het ontstaan van nieuwe vriendschappen.
            <br><br>
            Discovering by doing, learn new techniques and ways to intergraded my personal design identity into a 'contest' object is for me the most exciting part of this project.
            This object should encouraged social interaction. By placing a sculpture into one of the holes and press it, a sentences will appear. The user can answer the question, if the combination is right.  The questions are based on the past and the future. In my view the 'what if' questions are the most interesting because those confirm the reality. The collective use of this object can lead to honest, funny, deep and emotional talks, and maybe lead to new friendships..
            The exhibition of the Pleyade Design contest will be from 21 October until 14 November.
